public class booleanvar {
    public static void main(String[] args) {
        
        boolean a ;
        a = false;

        if (a) {
            System.out.println("a is true");
        } else {
            System.out.println("a is false");
        }

        System.out.println("Interchanging the value of a");

        a = true ;
        if (a) {
            System.out.println("a is true");
        } else {
            System.out.println("a is false");
        }
    }
}
